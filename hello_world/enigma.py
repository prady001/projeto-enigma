def gerar_matrizes_de_permutacao(N : int) -> Tuple[np.ndarray, np.ndarray]:
    """
    Gera duas matrizes de permutação de tamanho N x N.
    """

    I = np.eye(N)
    P = np.random.permutation(I)
    Q = np.random.permutation(I)

    return P, Q

